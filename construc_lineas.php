                        <p align="justify">Las labores de construcción de líneas protegidas obedecen a criterios entendidos como pertinentes en cuestiones de criterio no solamente técnico o administrativo, sino más allá.
El factor de responsabilidad social es el principal determinante para definir el sentido de prioridad de los trabajos a ser llevados a cabo; bajo una mirada de compromiso con la prestación de servicios a los usuarios se ha llevado a cabo una serie de construcciones de líneas protegidas de Media Tensión, las cuales consisten en tender tramos de conductores especialmente fabricados: poseen varias capas de revestimiento de materiales de última tecnología, y cuentan con elementos separadores entre conductores, eliminando por completo la posibilidad de que las principales ternas alimentadoras sufran perturbaciones y/o daños causados por razones ajenas a las labores de operación y mantenimiento.
