                        <p align="justify">El  alto grado de previsibilidad impreso en la concreción de proyectos de gran envergadura concedió desde siempre a CLYFSA situarse como actor de relevancia en el amplio sentido de la expresión cuando de visión estratégica de crecimiento y evolución constante hablamos; las apuestas por empujar la calidad del servicio prestado a mayores alcances sociales confirman la óptima respuesta brindada como compañía de distribución a las necesidades conocidas en las áreas de concesión con potencial de desarrollo como expansiones de la penetración demográfica obtenida por la Red Eléctrica de CLYFSA.
El crecimiento ordenado y estudiado como caso base para todas nuestras labores de planificación consiste en un aumento de la demanda total de la ciudad en torno a 8% por año; según opinión de nuestros Gerentes y Asesores, dicho crecimiento constituye un indicador de muy óptima gestión de la demanda de energía eléctrica. A su vez, los pronósticos de crecimiento son aún más alentadores: con la puesta en servicio de las 02 posiciones de transformación de potencia en la Sub Estación 66/23kV, CLYFSA duplicará sin más demora su potencia instalada histórica, lo cual es objeto de estudio de las instancias de análisis y profundización sobre los escenarios que esta posibilidad trae consigo.
 </p>
